const numbers = [1, 2, 3, 1, 4];

console.log('indexOf(1): ' + numbers.indexOf(1));
console.log('indexOf(1, 2): ' + numbers.indexOf(1, 2));
console.log('lastIndexOf(1): ' + numbers.lastIndexOf(1));
